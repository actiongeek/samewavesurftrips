// import
export const PickIcon = icon => {
  switch (icon) {
    case 'Goofy':
      return '/images/stance/goofy.svg'
    case 'Regular':
      return '/images/stance/regular.svg'
    case 'Shortboard':
      return '/images/board/Short_board.svg'
    case 'Longboard':
      return '/images/board/Long_board.svg'
    case 'Kneeboard':
      return '/images/board/Knee_board.svg'
    case 'Bodyboard':
      return '/images/board/Body_board.svg'
    case 'Stand Up':
      return '/images/board/SUP.svg'
    case 'Wind Surf':
      return '/images/board/Wind_surf.svg'
    case 'Kite Surf':
      return '/images/board/Kite_surf.svg'
    case 'Beginner':
      return '/images/level/Beginner.svg'
    case 'Pre-Intemediate':
      return '/images/level/Pre_intermediate.svg'
    case 'Intemediate':
      return '/images/level/Intermediate.svg'
    case 'Upper-Intemediate':
      return '/images/level/Upper_intermediate.svg'
    case 'Advanced':
      return '/images/level/Advanced.svg'
  }
}
